function getRandomInt(min, max) {
	min = Math.ceil(min);
	max = Math.floor(max);
	return Math.floor(Math.random() * (max - min + 1)) + min;
}

function getMultipleRandomInt(min, max, numberOfInt) {
	var randomIntArray = [];
	while (randomIntArray.length < numberOfInt) {
		var currentRandomInt = getRandomInt(min, max);
		if (randomIntArray.indexOf(currentRandomInt) === -1) randomIntArray.push(
			currentRandomInt);
	}
	return randomIntArray;
}


